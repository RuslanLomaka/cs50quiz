def normalize_language(language: str | None) -> str:
    if not language:
        return DEFAULT_LANGUAGE
    language = language.lower().split("-")[0]
    return language if language in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def detect_language(accept_language: str) -> str:
    lowered = accept_language.lower()
    for language in SUPPORTED_LANGUAGES:
        if language in lowered:
            return language
    return DEFAULT_LANGUAGE


def get_request_language(request) -> str:
    session_language = normalize_language(request.session.get("language"))

    if request.user.is_authenticated:
        preference = getattr(request.user, "quizforger_preference", None)
        if preference:
            return normalize_language(preference.language)

    if "language" in request.session:
        return session_language

    detected = detect_language(request.META.get("HTTP_ACCEPT_LANGUAGE", ""))
    request.session["language"] = detected
    return detected


def get_ui_text(language: str) -> dict:
    return UI_TEXT.get(normalize_language(language), UI_TEXT[DEFAULT_LANGUAGE])


def get_prompt_text(language: str) -> str:
    return PROMPTS.get(normalize_language(language), PROMPTS[DEFAULT_LANGUAGE])


def get_quiz_ui_text(language: str) -> dict:
    return QUIZ_UI_TEXT.get(normalize_language(language), QUIZ_UI_TEXT[DEFAULT_LANGUAGE])
